// Push notifications
self.addEventListener('push', (event) => {
  const options = {
    body: event.data ? event.data.text() : 'New notification from SkillSwap DAO',
    icon: '/SkillSwap DAO Logo Design.png',
    badge: '/favico.ico',
    vibrate: [100, 50, 100],
    data: {
      dateOfArrival: Date.now(),
      primaryKey: 1
    },
    actions: [
      {
        action: 'explore',
        title: 'Explore Skills',
        icon: '/favico.ico'
      },
      {
        action: 'close',
        title: 'Close',
        icon: '/favico.ico'
      }
    ]
  };

  event.waitUntil(
    self.registration.showNotification('SkillSwap DAO', options)
  );
});

// Notification click handler
self.addEventListener('notificationclick', (event) => {
  event.notification.close();

  if (event.action === 'explore') {
    event.waitUntil(
      clients.openWindow('/marketplace')
    );
  }
}); 